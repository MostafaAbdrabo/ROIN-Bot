async def cert_employment_handler(update, context):
    """Employment cert: language selection → generate PDF."""
    q = update.callback_query; await q.answer()
    ec = _find_ec(str(q.from_user.id))
    if not ec:
        await q.edit_message_text("❌ Not registered.", reply_markup=InlineKeyboardMarkup([[bm()]])); return
    context.user_data["cert_ec"] = ec
    kb = [
        [InlineKeyboardButton(LANG_LABELS["EN"], callback_data="cert_emp_EN")],
        [InlineKeyboardButton(LANG_LABELS["RU"], callback_data="cert_emp_RU")],
        [InlineKeyboardButton(LANG_LABELS["AR"], callback_data="cert_emp_AR")],
        [InlineKeyboardButton("↩️ Back", callback_data="menu_certificates"), bm()],
    ]
    await q.edit_message_text(
        "📜 Employment Confirmation\n\nSelect language:",
        reply_markup=InlineKeyboardMarkup(kb))
    return CERT_LANG


async def cert_emp_lang_sel(update, context):
    """Generate and send the employment cert PDF."""
    q = update.callback_query; await q.answer()
    lang = q.data.replace("cert_emp_", "")
    ec   = context.user_data.get("cert_ec")
    await q.edit_message_text("⏳ Generating certificate...")
    try:
        emp = _get_emp(ec)
        if not emp:
            await q.edit_message_text("❌ Employee record not found.",
                reply_markup=InlineKeyboardMarkup([[InlineKeyboardButton("↩️ Back", callback_data="menu_certificates"), bm()]])); return ConversationHandler.END
        pdf_bytes = _generate_employment_cert(emp, lang)
        filename  = f"{ec}-Employment-{datetime.now().strftime('%d%m%Y')}.pdf"
        from drive_utils import upload_to_drive
        drive_url = upload_to_drive(pdf_bytes, filename, "certificates")
        if drive_url:
            await q.edit_message_text(
                f"✅ Certificate generated!\nLanguage: {LANG_LABELS.get(lang, lang)}",
                reply_markup=InlineKeyboardMarkup([
                    [InlineKeyboardButton("📄 View PDF", url=drive_url)],
                    [InlineKeyboardButton("↩️ Back", callback_data="menu_certificates"), bm()]]))
        else:
            await q.message.reply_document(
                document=io.BytesIO(pdf_bytes),
                filename=filename,
                caption=f"📜 Employment Confirmation Certificate ({LANG_LABELS.get(lang, lang)})")
            await q.edit_message_text(
                f"✅ Certificate generated!\nLanguage: {LANG_LABELS.get(lang, lang)}",
                reply_markup=InlineKeyboardMarkup([
                    [InlineKeyboardButton("↩️ Back", callback_data="menu_certificates"), bm()]]))
    except Exception as e:
        await q.edit_message_text(f"❌ Error: {e}",
            reply_markup=InlineKeyboardMarkup([[InlineKeyboardButton("↩️ Back", callback_data="menu_certificates"), bm()]]))
    return ConversationHandler.END


async def cert_salary_handler(update, context):
    q = update.callback_query; await q.answer()
    ec = _find_ec(str(q.from_user.id))
    emp = _get_emp(ec) if ec else None
    # Check if HR has enabled salary cert for this employee
    # Field: Has_Salary_Cert_Enabled or similar — check Annual_Leave_Balance as proxy
    # For now check if employee record exists and has a note "salary_cert:yes"
    notes = str(emp.get("Notes", "")).lower() if emp else ""
    enabled = "salary_cert:yes" in notes or "salary_cert:1" in notes
    if enabled:
        await q.edit_message_text(
            "💰 Salary Certificate\n\nYour salary certificate request has been noted.\n"
            "Please collect it from the HR office.",
            reply_markup=InlineKeyboardMarkup([
                [InlineKeyboardButton("↩️ Back", callback_data="menu_certificates"), bm()]]))
    else:
        await q.edit_message_text(
            "💰 Salary Certificate\n\n⚠️ This certificate requires HR authorization.\n\n"
            "Please contact HR via 💬 Contact HR or visit the HR office to request this certificate.\n"
            "HR will enable it for you once approved.",
            reply_markup=InlineKeyboardMarkup([
                [InlineKeyboardButton("↩️ Back", callback_data="menu_certificates"), bm()]]))


