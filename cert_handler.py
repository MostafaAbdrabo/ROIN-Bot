def _find_ec(tid):
    for i, r in enumerate(get_sheet("User_Registry").get_all_values()):
        if i == 0: continue
        if r[1].strip() == str(tid): return r[0].strip()
    return None


def _get_emp(ec):
    for r in get_sheet("Employee_DB").get_all_records():
        if str(r.get("Emp_Code", "")).strip() == str(ec):
            return r
    return None


def _logo_path():
    p = os.path.join(CERT_BASE_DIR, "company_logo.png")
    return p if os.path.exists(p) else None


# ── PDF generation ─────────────────────────────────────────────────────────────

def _safe(text):
    if not text: return ""
    text = str(text)
    for old, new in {"\u2014": "-", "\u2013": "-", "\u2018": "'", "\u2019": "'",
                     "\u201c": '"', "\u201d": '"', "\u00a0": " "}.items():
        text = text.replace(old, new)
    return text.encode('latin-1', errors='replace').decode('latin-1')


class CertPDF(FPDF):
    def __init__(self, logo_path=None):
        super().__init__()
        self.logo_path = logo_path

    def header(self):
        if self.logo_path:
            self.image(self.logo_path, x=85, y=8, w=25)
            self.ln(28)
        else:
            self.ln(10)
        self.set_font("Helvetica", "B", 14)
        self.cell(0, 6, "ROIN WORLD FZE EGYPT BRANCH", new_x="LMARGIN", new_y="NEXT", align="C")
        self.set_font("Helvetica", "", 8)
        self.cell(0, 5, _safe("Building No 1, Gamal Abdel Nasser Street - El Dabaa - Matrouh"),
                  new_x="LMARGIN", new_y="NEXT", align="C")
        self.cell(0, 5, "info.egypt@roinworld.com     www.roinworld.com",
                  new_x="LMARGIN", new_y="NEXT", align="C")
        self.line(15, self.get_y() + 2, 195, self.get_y() + 2)
        self.ln(6)

    def footer(self):
        self.set_y(-15)
        self.set_font("Helvetica", "I", 7)
        self.cell(0, 4, "Generated by ROIN WORLD FZE HR System", align="C", new_x="LMARGIN", new_y="NEXT")
        self.cell(0, 4, _safe(f"Generated: {datetime.now().strftime('%d/%m/%Y %H:%M')}"), align="C")


def _generate_employment_cert(emp, lang="EN"):
    pdf = CertPDF(logo_path=_logo_path())
    add_dejavu(pdf)
    pdf.add_page()

    # Choose font family: DejaVu supports Cyrillic; Helvetica for Latin-only content
    ff = "DejaVu" if lang == "RU" else "Helvetica"
    s  = (lambda t: str(t) if t else "") if lang == "RU" else _safe

    if lang == "EN":
        title   = "EMPLOYMENT CONFIRMATION CERTIFICATE"
        to_whom = "TO WHOM IT MAY CONCERN"
        body    = (
            f"This is to certify that {s(emp.get('Full_Name',''))} "
            f"(Employee Code: {s(str(emp.get('Emp_Code','')))}) "
            f"is employed at ROIN WORLD FZE as {s(emp.get('Job_Title',''))} "
            f"in the {s(emp.get('Department',''))} Department.\n\n"
            f"Employment Start Date: {s(emp.get('Hire_Date',''))}\n"
            f"Contract Type: {s(emp.get('Contract_Type',''))}\n\n"
            f"This certificate is issued upon the request of the employee for all "
            f"official purposes.")
    elif lang == "RU":
        title   = "СПРАВКА О ТРУДОУСТРОЙСТВЕ"
        to_whom = "КОМУ СЛЕДУЕТ"
        body    = (
            f"Настоящим удостоверяем, что {s(emp.get('Full_Name',''))} "
            f"(Код сотрудника: {s(str(emp.get('Emp_Code','')))}) "
            f"является сотрудником компании ROIN WORLD FZE на должности "
            f"{s(emp.get('Job_Title',''))} в отделе {s(emp.get('Department',''))}.\n\n"
            f"Дата начала работы: {s(emp.get('Hire_Date',''))}\n"
            f"Тип контракта: {s(emp.get('Contract_Type',''))}\n\n"
            f"Данная справка выдана по запросу сотрудника для официальных целей.")
    else:  # AR — rendered left-to-right with latin font (limitation without Arabic font)
        title   = "SHAHADAT TAAKEED AL-ISTIKHDAAM"
        to_whom = "ILA MAN YA'HUMMAHU AL-AMR"
        body    = (
            f"Nushhad bi-anna {s(emp.get('Full_Name',''))} "
            f"(Code: {s(str(emp.get('Emp_Code','')))}) "
            f"ya'mal ladayna fi ROIN WORLD FZE "
            f"bi-safatihi {s(emp.get('Job_Title',''))} "
            f"fi qism {s(emp.get('Department',''))}.\n\n"
            f"Tarikh al-tawdhif: {s(emp.get('Hire_Date',''))}\n"
            f"Naw' al-aqd: {s(emp.get('Contract_Type',''))}\n\n"
            f"Sudira hadha al-shahadat bi-talab al-muwazzaf li-jami' al-agh-rad al-rasmiyya.")

    pdf.set_font(ff, "B", 16)
    pdf.cell(0, 10, title, new_x="LMARGIN", new_y="NEXT", align="C")
    pdf.ln(3)
    pdf.set_font(ff, "B", 11)
    pdf.cell(0, 8, to_whom, new_x="LMARGIN", new_y="NEXT", align="C")
    pdf.ln(6)

    pdf.set_font(ff, "", 11)
    pdf.set_fill_color(230, 230, 230)
    details_label = "  СВЕДЕНИЯ О СОТРУДНИКЕ" if lang == "RU" else "  EMPLOYEE DETAILS"
    pdf.cell(0, 8, details_label, new_x="LMARGIN", new_y="NEXT", fill=True)
    pdf.ln(3)
    if lang == "RU":
        field_labels = [("Имя", "Full_Name"), ("Код", "Emp_Code"),
                        ("Отдел", "Department"), ("Должность", "Job_Title"),
                        ("Гражданство", "Nationality"), ("Дата найма", "Hire_Date"),
                        ("Тип контракта", "Contract_Type")]
    else:
        field_labels = [("Name", "Full_Name"), ("Code", "Emp_Code"),
                        ("Department", "Department"), ("Job Title", "Job_Title"),
                        ("Nationality", "Nationality"), ("Hire Date", "Hire_Date"),
                        ("Contract Type", "Contract_Type")]
    for label, key in field_labels:
        pdf.set_font(ff, "B", 10)
        pdf.cell(50, 7, f"  {label}:")
        pdf.set_font(ff, "", 10)
        pdf.cell(0, 7, s(str(emp.get(key, "-"))), new_x="LMARGIN", new_y="NEXT")
    pdf.ln(6)

    pdf.set_font(ff, "", 10)
    for para in body.split("\n"):
        pdf.multi_cell(0, 7, s(para) if para else "")
    pdf.ln(10)

    pdf.set_font(ff, "", 10)
    today = datetime.now().strftime("%d/%m/%Y")
    date_label = f"Дата: {today}" if lang == "RU" else f"Date: {today}"
    sig_label  = "Подпись:" if lang == "RU" else "Authorized Signature:"
    pdf.cell(95, 7, date_label)
    pdf.cell(95, 7, sig_label, align="R", new_x="LMARGIN", new_y="NEXT")
    pdf.ln(2)

    # Embed HR Manager signature if provided
    hr_sig_bytes = emp.get("_hr_sig_bytes") if emp else None
    hr_sig_text  = emp.get("_hr_sig_text",  "") if emp else ""
    if hr_sig_bytes:
        try:
            img_io = io.BytesIO(hr_sig_bytes)
            pdf.image(img_io, x=pdf.w - pdf.r_margin - 45, y=pdf.get_y(), h=12)
            pdf.ln(14)
        except Exception:
            pdf.set_font(ff, "I", 8)
            pdf.cell(0, 5, s(hr_sig_text or "HR Manager"), align="R",
                     new_x="LMARGIN", new_y="NEXT")
    elif hr_sig_text:
        pdf.set_font(ff, "I", 8)
        pdf.cell(0, 5, s(hr_sig_text), align="R", new_x="LMARGIN", new_y="NEXT")
    else:
        pdf.cell(0, 7, "___________________", align="R", new_x="LMARGIN", new_y="NEXT")

    hr_title = "Менеджер по персоналу — ROIN WORLD FZE" if lang == "RU" else "HR Manager — ROIN WORLD FZE"
    pdf.set_font(ff, "", 10)
    pdf.cell(0, 7, hr_title, align="R", new_x="LMARGIN", new_y="NEXT")
    pdf.ln(3)
    ver_code = f"SIG-{secrets.token_hex(4).upper()}"
    pdf.set_font(ff, "I", 7)
    pdf.cell(0, 4, f"Verified: {ver_code}", align="C")

    return pdf.output()


