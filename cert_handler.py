async def cert_back_to_menu(update, context):
    from bot import build_inline_menu
    q = update.callback_query; await q.answer()
    role = "Employee"
    for i, r in enumerate(get_sheet("User_Registry").get_all_values()):
        if i == 0: continue
        if r[1].strip() == str(q.from_user.id): role = r[3].strip(); break
    await q.edit_message_text("Choose an option:", reply_markup=build_inline_menu(role))
    return ConversationHandler.END


def get_cert_handler():
    return ConversationHandler(
        entry_points=[CallbackQueryHandler(cert_employment_handler, pattern="^cert_employment$")],
        states={
            CERT_LANG: [
                CallbackQueryHandler(cert_emp_lang_sel,  pattern="^cert_emp_(EN|AR|RU)$"),
                CallbackQueryHandler(cert_back_to_menu,  pattern="^back_to_menu$"),
                CallbackQueryHandler(cert_menu_handler,  pattern="^menu_certificates$"),
            ],
        },
        fallbacks=[MessageHandler(filters.COMMAND, cert_back_to_menu)],
        per_message=False,
    )


def get_cert_static_handlers():
    """Non-conversation handlers for cert menu and simple cert screens."""
